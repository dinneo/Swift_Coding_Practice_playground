# 大量Data資料結構：
#### Queue / Stack
_____________________
## Stack

![](Data12.png "")

繁中「堆疊」，簡中「栈」。像疊盤子，顛倒資料前後順序。

![](Data13.png "")

Array和List皆可實作。

插入、刪除需時**O(1)**。搜尋需時**O(N)**。

堆疊有反轉的性質、有括號對應的性質、有遞迴與疊代的性質。

可以直接使用STL的stack。

