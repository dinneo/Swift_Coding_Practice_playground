# 關於Swift程式語言

Swift是一種新的程式語言，用於編寫iOS、OS X、watchOS與tvOS應用程序。Swift結合了C和Objective-C的優點並且不受C相容性的限制。Swift採用安全的編程模式並添加了很多新特性，這將使編程更簡單，更靈活，也更有趣。Swift是基於成熟而且倍受喜愛的Cocoa和Cocoa Touch框架，它的降臨將使軟體開發獲得重新想像的機會。

Swift程式語言已開發許多年。為了給Swift打好基礎，蘋果公司改進了編譯器、除錯工具和框架基礎架構。我們使用自動引用計數（Automatic Reference Counting, ARC）來簡化內存管理。我們在Foundation和Cocoa穩固的基礎架構上建構與堆砌框架使其完全現代化和標準化。Objective-C本身支持塊、集合語法和模塊，所以框架可以輕鬆支援現代程式語言技術。正是得益於這些基礎工作，我們現在才能發布這樣一個用於未來蘋果軟件開發的新語言。

Objective-C開發者對Swift程式語言並不會感到陌生。它採用了Objective-C的命名參數以及動態對象模型，可以無縫對接到現有的Cocoa框架，並且可以相容於 Objective-C代碼。在此基礎之上，Swift程式語言還提供多新功能並且統一了語言中程序式和物件導向的部分。

Swift程式語言對於初學者來說也很友好。它是第一個既滿足工業標準又像腳本語言一樣充滿表現力和趣味的程式語言。它支援playground中的代碼預覽，這個創新的功能允許開發者在不編譯和執行應用程式的前提下對程式碼試做並即時查看結果。

Swift程式語言將現代程式語言的精華和蘋果工程師文化的智慧結合起來。編譯器對性能進行了最佳化，程式語言對開發進行了最佳化，兩者互不干擾，魚與熊掌兼得。Swift既可以用於開發"hello, world"這樣的小程序，也可以用於開發一套完整的操作系統。所有的這些特性讓Swift程式語言對於開發者和蘋果來說都是一項值得的投資。

Swift程式語言是編寫iOS、OS X、watchOS與tvOS應用的極佳方式，並將伴隨著新的特性和功能持續演進。我們對Swift程式語言充滿信心，迫不及待讓您使用來開發有趣的應用！



